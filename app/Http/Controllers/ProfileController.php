    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $file1 = $request->file('img');
        if(!empty($file1)){
            $filename1 = $file1->getClientOriginalName();
            $picture1 = date('His') . '-' . $filename1;
            $file1->move(public_path('uploads'), $picture1);
        }
        

        $details = User::whereid(Auth::user()->id)->first();
        if(isset($picture1))
        $details->userImg = $picture1;
        $details->save();
        return redirect()->action('ProfileController@index')->with('success', "Profile Successfully Updated");
    }

